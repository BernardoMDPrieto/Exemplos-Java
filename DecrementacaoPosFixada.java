package exemplos;

public class DecrementacaoPosFixada {
	public static void main(String[]args) {
		int valor = 10;
		
		//Decrementação pós fixada, ele primeiro mostra e depois decrementa
		System.out.println(valor--);//O valor continua como 10
		System.out.println(valor);//O valor vira 9
	}
}
