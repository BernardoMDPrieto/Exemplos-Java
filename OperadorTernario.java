package exemplos;

public class OperadorTernario {
	public static void main(String[]args) {
		int a = 2;
		int b = 5;
		int min = (a<b)?a:b;
		//Os elementos entre parenteses são a condição, depois da interrogação, são na ordem de condição verdadeira ou condição falsa
		System.out.println(min);
		
	}

}
