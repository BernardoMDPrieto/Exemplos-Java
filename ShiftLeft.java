package exemplos;

public class ShiftLeft {
	public static void main(String[]args) {
		//O número da direita será elevado ao quadrado em seguida será multiplicado pelo da esquerda.
		//Sendo então 10<<2 = 2^2 = 4 * 10 = 40
		//Eleva ao quadrado e depois multiplica.
		System.out.println(10<<2);
		System.out.println(10<<3);
		System.out.println(20<<2);
		System.out.println(15<<4);
	}
}
