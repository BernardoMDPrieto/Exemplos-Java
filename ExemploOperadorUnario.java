package exemplos;

public class ExemploOperadorUnario {
	public static void main(String []args) {
		int valorA = 10;
		int valorB = -10;
		//Neste exemplo ele irá executar duas operações
		//Primeiro ele multiplica por -1
		//Depois subtrai por 1
		System.out.println(~valorA);//-11
		System.out.println(~valorB);//9
	}
}
